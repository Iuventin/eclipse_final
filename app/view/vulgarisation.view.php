<div class="brassage">
    <div class="sous-etape">
        <h2>Concassage</h2>
        <p>
            Cette étape a pour but de broyer et concasser notre assemblage de grains de malt sélectionnés en petits
            morceaux à l’aide d’un moulin à malt ou broyeur. Cette partie permet de libérer le grain pour que
            l’activité des enzymes sur les sucres en empatâge soit améliorée.
        </p>
    </div>


    <div class="sous-etape">
        <h2>Empâtage</h2>
        <p>
            La <em>mouture</em> (grain concassé/broyé) est placée dans une cuve filtrante et est mélangée avec de
            l’eau
            chaude. L’empâtage se fait par infusion tout en remuant le mélange
        </p>
    </div>


    <div class="sous-etape">
        <h2>Filtration</h2>
        <p>
            La filtration permet de séparer le “jus” des grains qu’on appelle le moût, pour récupérer ce qui nous
            intéresse, puis on verse de l’eau chaude pour récupérer un maximum de moût et de sucre car le tout va
            s’égoutter.
        </p>
    </div>


    <div class="sous-etape">
        <h2>Ébullition</h2>
        <p>
            à 100°C pour pouvoir désinfecter celui-ci et ajouter nos houblons et nos épices. Donc cela va éliminer
            les microorganismes indésirables, extraire les acides et arômes des houblons infusés.
        </p>
    </div>


    <div class="sous-etape">
        <h2>Clarification</h2>
        <p>
            cette étape sert à séparer le houblon/épices restant du moût, on fait infuser dans un sac et un filtre
            donc nous avions juste à le sortir de la cuve.
        </p>
    </div>


    <div class="sous-etape">
        <h2>Refroidissement</h2>
        <p>
            Le refroidissement se fait avec un serpentin ou l’eau passe à travers pour refroidir à l’intérieur. On
            mélange pour répartir la température et le refroidissement se fait rapidement pour éviter que des
            paliers de chaleurs soient trop longs pour permettre une contamination. Et notre brassage est terminé.
        </p>
    </div>
</div>

